#include <algorithm>
#include <cmath>
#include <iostream>
using namespace std;
int n, x, y, z, now, ans;
int main()
{
    int t;
    scanf("%d", &t);
    while (t--)
    {
        ans = now = 0;
        scanf("%d%d%d%d", &n, &x, &y, &z);
        while (now < z)
        {
            ans++;            
        }
    }
    return 0;
}